package control_flow.q4;

public class Question4 {
    public static void main(String[] args) {
        int input = 4;
        switch (input) {
            case 1:
                System.out.println("the input is 1");
                break;
            case 2:
                System.out.println("the input is 2");
                break;
            case 3:
                System.out.println("the input is 3");
                break;
            case 4:
                System.out.println("the input is 4");
                break;
            case 5:
                System.out.println("the input is 5");
                break;
            default:
                System.out.println("the input is not a number between 1 and 5");
        }
    }
}
